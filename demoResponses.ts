const DEFAULT_REPLY = `ご依頼ありがとうございます。

営業AIとして、営業メールの作成、提案書のたたき台、価格交渉時のトーク作成などを承れます。

例えば、以下のような依頼が可能です。
・「新規顧客への初回アプローチメールを書いて」
・「提案書の構成案を作って」
・「値引き交渉への切り返しトークが欲しい」

具体的に依頼内容を教えていただけますか?

※これはデモ応答です。実際の利用では、貴社の商材・状況に合わせた回答を生成します。`;

export function getDemoReply(userMessage: string): string {
  const lower = userMessage.toLowerCase();

  for (const pattern of DEMO_PATTERNS) {
    if (pattern.keywords.some((k) => lower.includes(k.toLowerCase()))) {
      return pattern.reply;
    }
  }

  return DEFAULT_REPLY;
}
